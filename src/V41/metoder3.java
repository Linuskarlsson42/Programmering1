package V41;

import java.util.Scanner;

public class metoder3 {
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		
		int n = input.nextInt();
		count(n);
		
	}//end
	public static void count(int n) {
		for (int i = 1; i <= n; i++) {
			System.out.println(i);
		}
		
		
	}//end
}//end
